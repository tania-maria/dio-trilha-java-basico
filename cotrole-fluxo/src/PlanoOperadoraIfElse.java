public class PlanoOperadoraIfElse {
    public static void main(String[] args) {

        String plano = "M"; // M / T
        if (plano == "B") {
            System.out.println("100 minutos de ligações");
        } else if (plano == "M") {
            System.out.println("100 minutos de ligações");
            System.out.println("Whats e Intagram grátis");
        } else if (plano == "T") {
            System.out.println("100 minutos de ligações");
            System.out.println("Whats e Intagram grátis");
            System.out.println("5GB Youtube");

        }
    }
}
