public class TesteExercicio4 {
    public static void main(String[] args) {
        for (int x = 1; x <= 10; x++) { // faz o for até chegar no 10
            if (x % 2 == 0) // verifica se o numero é dividido por 2
                // % operador que representa módulo de uma divisão
                System.out.println(x);
        }

    }

}
