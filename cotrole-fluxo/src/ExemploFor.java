public class ExemploFor {
    public static void main(String[] args) {
        // for (bloco de inicialização; expressão booleana de validação; bloco de
        // atualização )
        for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos++) {
            System.out.println("Contando carneirinhos" + carneirinhos);
        }
        // verificado se o codigo está certo e não vai rodar mais nada .
        System.out.println("Joaozinho dormiu");
    }

}

// estrutura do controle de fluxo for
/*
 * for (bloco de inicialização; expressão booleana de validação; bloco de
 * atualização )
 * {
 * comando que será executado até que a expressão de validação torne-se falsa
 * }
 */